</style>

<script>
const slider = document.getElementById('scrollableRow');
let isDown = false;
let startX;
let scrollLeft;

// ----- Mouse Drag Scroll -----
slider.addEventListener('mousedown', e => {
    isDown = true;
    slider.classList.add('cursor-grabbing');
    startX = e.pageX - slider.offsetLeft;
    scrollLeft = slider.scrollLeft;
});
slider.addEventListener('mouseleave', () => { isDown = false; slider.classList.remove('cursor-grabbing'); });
slider.addEventListener('mouseup', () => { isDown = false; slider.classList.remove('cursor-grabbing'); });
slider.addEventListener('mousemove', e => {
    if (!isDown) return;
    e.preventDefault();
    const x = e.pageX - slider.offsetLeft;
    slider.scrollLeft = scrollLeft - (x - startX) * 2; // drag speed
});

// ----- Continuous Auto Scroll -----
let scrollAmount = 1; // adjust speed here
setInterval(() => {
    slider.scrollLeft += scrollAmount;
    if (slider.scrollLeft + slider.offsetWidth >= slider.scrollWidth) {
        slider.scrollLeft = 0; // loop to start
    }
}, 20); // lower value = faster scroll
</script>
